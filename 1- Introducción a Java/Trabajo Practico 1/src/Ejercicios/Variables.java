package Ejercicios;

public class Variables {

    public static void main(String[] args) {
        String nombre;
        int edad;
        double altura;
        boolean estudiante;

        nombre = "Andres";
        edad = 38;
        altura = 1.83;
        estudiante = true;

        System.out.println("Nombre: " + nombre);
        System.out.println("Edad: " + edad);
        System.out.println("Altura: " + altura);
        System.out.println("Estudiante: " + estudiante);
    }
}
